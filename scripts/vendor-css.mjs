// Generate styles.css = @xterm/xterm's stylesheet + our plugin styles.
// xterm.js will not render correctly without its CSS, and Obsidian only loads
// the plugin's single styles.css — so we concatenate the two here. Always
// regenerates from source, so it is safe to run repeatedly.
import fs from "fs";

const XTERM_CSS = "node_modules/@xterm/xterm/css/xterm.css";
const SRC = "styles.src.css";
const OUT = "styles.css";

if (!fs.existsSync(XTERM_CSS)) {
	console.error(
		`vendor-css: ${XTERM_CSS} not found. Run \`npm install\` first.`,
	);
	process.exit(1);
}

const xterm = fs.readFileSync(XTERM_CSS, "utf8");
const src = fs.readFileSync(SRC, "utf8");
const header = "/* GENERATED by scripts/vendor-css.mjs — edit styles.src.css */\n";
fs.writeFileSync(OUT, `${header}${xterm}\n${src}`);
console.log("vendor-css: wrote styles.css (xterm.css + styles.src.css)");
